package ru.alisa.calcapp.util;

public class Calculator {

    public static int added(int num1, int num2) {
        int summ = num1 + num2;
        return summ;
    }

    public static int deduct(int num1, int num2) {
        int diff = num1 - num2;
        return diff;

    }

    public static int multiply(int num1, int num2) {
        int multi = num1 * num2;
        return multi;

    }


}
